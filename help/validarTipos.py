algo = input('Digite algo')

print('Esse algo é do tipo number', algo.isalnum())
print('Esse algo é do tipo string', algo.isalpha())
print('Esse algo é do tipo decimal', algo.isdecimal())
print('Esse algo é do tipo digito', algo.isdigit())
print('Esse algo é do tipo numerico', algo.isnumeric())
print('Esse algo é do tem espaço', algo.isspace())
print('Esse algo é do tem string maiuscula', algo.isupper())